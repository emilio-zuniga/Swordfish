pub fn get_piece_key(piece: PieceType, color: Color, square: Square) -> u64 {
    let piece_type_offset = match color {
        Color::Black => match piece {
            PieceType::Pawn => 0,
            PieceType::Knight => 2,
            PieceType::Bishop => 4,
            PieceType::Rook => 6,
            PieceType::Queen => 8,
            PieceType::King => 10,
            PieceType::Super => panic!("We should never look for a super piece!"),
        },
        Color::White => match piece {
            PieceType::Pawn => 1,
            PieceType::Knight => 3,
            PieceType::Bishop => 5,
            PieceType::Rook => 7,
            PieceType::Queen => 9,
            PieceType::King => 11,
            PieceType::Super => panic!("We should never look for a super piece!"),
        },
    };
    let row = (square.to_u64().trailing_zeros() / 8) as usize;
    let file = (7 - (square.to_u64().trailing_zeros() % 8)) as usize;

    let offset = 64 * piece_type_offset + 8 * row + file;

    POLYGLOT_ARRAY[offset]
}

pub fn get_castling_key(rights: CastlingRecord) -> u64 {
    let offset = 768;
    let mut key = 0;
    match rights.white {
        CastlingRights::Both => key = key ^ POLYGLOT_ARRAY[offset] ^ POLYGLOT_ARRAY[offset + 1],
        CastlingRights::Kingside => key = key ^ POLYGLOT_ARRAY[offset],
        CastlingRights::Queenside => key = key ^ POLYGLOT_ARRAY[offset + 1],
        CastlingRights::Neither => (),
    }
    match rights.black {
        CastlingRights::Both => key = key ^ POLYGLOT_ARRAY[offset + 2] ^ POLYGLOT_ARRAY[offset + 3],
        CastlingRights::Kingside => key = key ^ POLYGLOT_ARRAY[offset + 2],
        CastlingRights::Queenside => key = key ^ POLYGLOT_ARRAY[offset + 3],
        CastlingRights::Neither => (),
    }

    key
}

pub fn get_en_passant_key(target: Square) -> u64 {
    let offset = 772;
    let file = (7 - (target.to_u64().trailing_zeros() % 8)) as usize;

    POLYGLOT_ARRAY[offset + file]
}

pub fn get_turn_key(white_to_move: bool) -> u64 {
    if white_to_move {
        POLYGLOT_ARRAY[780]
    } else {
        0
    }
}

